        <p style="font-size: 1.4em">
            Ces deux projets ont été réalisés lors de nos cours de .Net, dans un premier temps j'ai conçus un jeux de de
            Dames marchant en ligne de commande sans graphismes.
            <br>
            Tous les déplacements respectent les règles du jeu, les joueurs jouent l'un après l'autre et la partie
            prend fin lorsqu’un joueur a supprimer tous les pions de l'adversaire. Pour finir le jeu était complétement
            fonctionnel, la visualisation du plateau était possible grâce à la création de tracés et la disposition des
            pions à chaque début de tour. <br>
            Le lecteur de sous-titre permet de lire les fichiers srt, une fois le fichier choisi, le texte apparait en
            fonction du temps adapté.

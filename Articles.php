        <em style="font-size: 2em">Premier projet sous Symfony 2.8</em>
        <em style="font-size: 1.3em">Date : 2016 <br> Langage(s) : HTML, CSS, JS, PHP, Framework Symfony et Bootstrap
            <br> Adresse : <a
                    href="https://github.com/ThomasBerranger/Parc-Informatique" style="border-bottom: solid"
                    target="_blank">GitHub ThomBerranger</a></em>
        <p style="font-size: 1.4em">
            Le parc informatique était un projet scolaire, nous devions créer une application web comprenant une gestion
            d'utlisateur et leur permettant de créer/lire/modifier/supprimer des pièce et des machines.
            <br>
            Une pièce pouvait contenir autant de machines que nécessaire, les machines disposaient d'un ip unique.
            Machines ainsi que pièces étaient personnalisées par des noms, des couleurs etc...
            <br>
            Un utilisateur pouvait tester la connectivité d'une machine en entrant son ip, il pouvait ainsi savoir si la
            machine était bien en fonction ou si elle était éteinte ou encore si cet ip n'était pas encore enregistré.
            <br>
            Ce projet fût un succès et me créa un réel interêt pour Symfony et les framework php.
        </p>
